#include <iostream>
#include <string>
#include <vector>
using namespace std;
int main() {
	string s;
	cin >> s;
	int sum = 0;
	for (const char &c : s) {
		int t = c - '0';
		sum += t;
	}
	int arr[3] = { 0 };
	int count = 0;
	while (sum != 0) {
		arr[count] = sum % 10;
		sum /= 10;
		count++;
	}
	vector<string> outArr = { "ling","yi","er","san","si","wu","liu","qi","ba","jiu" };
	for (int i = count - 1; i > -1; i--) {
		
		cout << outArr[arr[i]];
		if (i != 0)
			cout << ' ';
	}
	system("pause");
}
/*
读入一个自然数n，计算其各位数字之和，用汉语拼音写出和的每一位数字。

输入格式：每个测试输入包含1个测试用例，即给出自然数n的值。这里保证n小于10100。

输出格式：在一行内输出n的各位数字之和的每一位，拼音数字间有1 空格，但一行中最后一个拼音数字后没有空格。

输入样例：
1234567890987654321123456789
输出样例：
yi san wu
*/